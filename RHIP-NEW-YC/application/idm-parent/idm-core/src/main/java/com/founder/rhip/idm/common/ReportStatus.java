package com.founder.rhip.idm.common;

/**
 * 报卡审批状态
 * @version 1.0, 2013-2-26
 */
public enum ReportStatus {
	
	/**
	 * 待审核
	 */
	NOT_VERIFY(1),
	/**
	 * 审核通过
	 */
	HOSPITAL_VERIFY(2),
	/**
	 * 已作废
	 */
	CANCEL(3);
	
	
	
	/**
	 * 上报后初始状态,防保科待审核
	 *//*
	READY(1),
	*//**
	 *  防疫科退回
	 *//*
	REJECT(2),
	*//**
	 * 防疫科待审核
	 *//*
	VERIFIED_FIRST(3),
	*//**
	 * 防疫科审核通过
	 *//*
	VERIFIED_SECOND(4),
	*//** 
	 * 防保科作废
	 *//*
	CANCEL(5);*/
	
   private Integer value;
   
   ReportStatus(Integer value) {
        this.value = value;
    }

   public Integer getValue() {
       return this.value;
   }
}
