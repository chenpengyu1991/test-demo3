public enum StatisticsType {
	/**
	 * 传染病管理月报表-传染病访视月报表
	 */
	INTERVIEW(1),
	/**
	 * 传染病管理月报表-细菌性痢疾流调月报表
	 */	
	DYSENTERY(2),
	/**
	 * 传染病管理月报表-狂犬病防治月报表
	 */	
	RABIES(3),
	/**
	 * 急性传染病防制
	 */	
	ACUTE(4);
	
  private Integer value;   
   
   StatisticsType(Integer value) {
        this.value = value;
    }

   public Integer getValue() {
       return this.value;
   }
}
