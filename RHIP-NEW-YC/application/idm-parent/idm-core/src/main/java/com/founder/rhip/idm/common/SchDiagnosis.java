/**
 * 血吸虫病诊断分期（个案调查）
 * @version 1.0, 2013-5-28
 */
public enum SchDiagnosis {
	/**
	 * 急性血吸虫
	 */
	ACUTE("1"),
	/**
	 * 慢性血吸虫
	 */
	CHRONIC("2"),	
	/**
	 * 晚期血吸虫
	 */
	LATER("3"),		
	/**
	 * 扩大治疗
	 */
	EXPANDED_TREAT("4"),
	/**
	 * 排除
	 */
	ELIMINATION("5");
   private String value;
   
   SchDiagnosis(String value) {
        this.value = value;
    }

   public String getValue() {
       return this.value;
   }
}
