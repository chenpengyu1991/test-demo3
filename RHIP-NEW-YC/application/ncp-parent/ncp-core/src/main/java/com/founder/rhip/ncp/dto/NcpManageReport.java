
package com.founder.rhip.ncp.dto;

/**
 * 健康教育报表对象
 * 
 * @author GaoFei
 *
 */
public class NcpManageReport {

	//管理机构编码
	private String manageOrgCode;

	//管理机构名称
	private String manageOrgName;

	//管理医生编码
	private String manageDoctorCode;

	//管理医生姓名
	private String manageDoctorName;

	//建卡次数
	private Long healthCardNum;

	//监测次数
	private Long monitorNum;

	//复查次数
	private Long reviewNum;

	//随访次数
	private Long followupNum;

	public String getManageOrgCode() {
		return manageOrgCode;
	}

	public void setManageOrgCode(String manageOrgCode) {
		this.manageOrgCode = manageOrgCode;
	}

	public String getManageOrgName() {
		return manageOrgName;
	}

	public void setManageOrgName(String manageOrgName) {
		this.manageOrgName = manageOrgName;
	}

	public String getManageDoctorCode() {
		return manageDoctorCode;
	}

	public void setManageDoctorCode(String manageDoctorCode) {
		this.manageDoctorCode = manageDoctorCode;
	}

	public String getManageDoctorName() {
		return manageDoctorName;
	}

	public void setManageDoctorName(String manageDoctorName) {
		this.manageDoctorName = manageDoctorName;
	}

	public Long getHealthCardNum() {
		return healthCardNum;
	}

	public void setHealthCardNum(Long healthCardNum) {
		this.healthCardNum = healthCardNum;
	}

	public Long getMonitorNum() {
		return monitorNum;
	}

	public void setMonitorNum(Long monitorNum) {
		this.monitorNum = monitorNum;
	}

	public Long getReviewNum() {
		return reviewNum;
	}

	public void setReviewNum(Long reviewNum) {
		this.reviewNum = reviewNum;
	}

	public Long getFollowupNum() {
		return followupNum;
	}

	public void setFollowupNum(Long followupNum) {
		this.followupNum = followupNum;
	}
}
