package com.founder.rhip.im.common;

/**
 * 报表常量定义
 * 
 * @author yejianfei
 * 
 */
public enum ReportDataType {
	Integer("Integer"),   //整形
	Double("Double"),   //浮点型
	Percent("Percent");  //百分数

	private String value;

	ReportDataType(String value){
		this.value=value;
	}

	public String getValue(){
		return this.value;
	}

}
