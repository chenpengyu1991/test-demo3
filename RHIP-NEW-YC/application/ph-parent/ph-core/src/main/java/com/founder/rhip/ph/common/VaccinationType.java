public enum VaccinationType {
    RABIES(1), //狂犬疫苗
    HEPATITIS(2),    //乙肝疫苗
    INFLUENZA(3),  //流感疫苗
    PNEUMONIA(4);  //23价肺炎疫苗

    private Integer value;

    VaccinationType(Integer value) {
        this.value = value;
    }

    public Integer getValue() {
        return this.value;
    }
}
