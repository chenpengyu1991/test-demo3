package com.founder.rhip.ph.dto.vaccine;

import java.io.Serializable;
import java.util.Date;

/**
 * 
 * @author xu_bin
 * main page use - CDC(Centers for Disease Control)
 * include(list vaccination records)
 */
public class VaccinationRecordsDTO implements Serializable {

    private static final long serialVersionUID = 5698474018716657433L;

	private Long id; //编号
	
	private String ehrId;//用于关联用户选择哪条接种记录
	
	private String name;//姓名
	
	private String gender;//性别
	
	private Integer age;//年龄
	
	private String idCard;//身份证号
	
	private String address;//现住址
	
	private String pastreet;
	
	private String patownShip;
	
	private String pahouseNumber;
	
	private String vaccinationType;//疫苗类型

	private Date vaccinationDate;//接种日期
	
	private String vaccinationUnitName;//接种地点
	
	private String immuType;//接种类型
	
	private String vaccineIndex;//接种次数
	
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getEhrId() {
		return ehrId;
	}

	public void setEhrId(String ehrId) {
		this.ehrId = ehrId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

	public String getIdCard() {
		return idCard;
	}

	public void setIdCard(String idCard) {
		this.idCard = idCard;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getPastreet() {
		return pastreet;
	}

	public void setPastreet(String pastreet) {
		this.pastreet = pastreet;
	}

	public String getPatownShip() {
		return patownShip;
	}

	public void setPatownShip(String patownShip) {
		this.patownShip = patownShip;
	}

	public String getPahouseNumber() {
		return pahouseNumber;
	}

	public void setPahouseNumber(String pahouseNumber) {
		this.pahouseNumber = pahouseNumber;
	}

	public String getVaccinationType() {
		return vaccinationType;
	}

	public void setVaccinationType(String vaccinationType) {
		this.vaccinationType = vaccinationType;
	}

	public Date getVaccinationDate() {
		return vaccinationDate;
	}

	public void setVaccinationDate(Date vaccinationDate) {
		this.vaccinationDate = vaccinationDate;
	}

	public String getVaccinationUnitName() {
		return vaccinationUnitName;
	}

	public void setVaccinationUnitName(String vaccinationUnitName) {
		this.vaccinationUnitName = vaccinationUnitName;
	}

	public String getImmuType() {
		return immuType;
	}

	public void setImmuType(String immuType) {
		this.immuType = immuType;
	}

	public String getVaccineIndex() {
		return vaccineIndex;
	}

	public void setVaccineIndex(String vaccineIndex) {
		this.vaccineIndex = vaccineIndex;
	}
	
}
