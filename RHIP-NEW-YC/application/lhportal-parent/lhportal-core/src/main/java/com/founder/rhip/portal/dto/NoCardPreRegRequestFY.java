package com.founder.rhip.portal.dto;

import com.founder.rhip.portal.common.TransCodeConstants;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

import java.io.Serializable;

@XmlRootElement(name = "Req")
@XmlAccessorType(XmlAccessType.FIELD)
public class NoCardPreRegRequestFY implements Serializable {

	private static final long serialVersionUID = 577024078443982329L;

	private String oracode = TransCodeConstants.ORACODE;

	private String oraauthcode = TransCodeConstants.ORAAUTHCODE;
	
	private String TransactionCode = TransCodeConstants.NO_CARD_PRE_REG;
	
	private String IDCardID;
	
	private String Mobile;
	
	private String Name;
	
	private String BirthDay;
	
	private String Sex;

	public String getOracode() {
		return oracode;
	}

	public void setOracode(String oracode) {
		this.oracode = oracode;
	}

	public String getOraauthcode() {
		return oraauthcode;
	}

	public void setOraauthcode(String oraauthcode) {
		this.oraauthcode = oraauthcode;
	}

	public String getTransactionCode() {
		return TransactionCode;
	}

	public void setTransactionCode(String transactionCode) {
		TransactionCode = transactionCode;
	}

	public String getIDCardID() {
		return IDCardID;
	}

	public void setIDCardID(String iDCardID) {
		IDCardID = iDCardID;
	}

	public String getMobile() {
		return Mobile;
	}

	public void setMobile(String mobile) {
		Mobile = mobile;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public String getBirthDay() {
		return BirthDay;
	}

	public void setBirthDay(String birthDay) {
		BirthDay = birthDay;
	}

	public String getSex() {
		return Sex;
	}

	public void setSex(String sex) {
		Sex = sex;
	}
	
}
