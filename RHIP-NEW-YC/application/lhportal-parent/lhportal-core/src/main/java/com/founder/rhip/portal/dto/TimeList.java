package com.founder.rhip.portal.dto;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import java.io.Serializable;

@XmlAccessorType(XmlAccessType.FIELD)
public class TimeList implements Serializable {

	private static final long serialVersionUID = 577024078443982329L;
	
	private String STime;
	
	private String ETime;
	
	private String PreOrNo;

    public String getPreOrNo() {
        return PreOrNo;
    }

    public void setPreOrNo(String preOrNo) {
        PreOrNo = preOrNo;
    }

    public String getSTime() {
        return STime;
    }

    public void setSTime(String STime) {
        this.STime = STime;
    }

    public String getETime() {
        return ETime;
    }

    public void setETime(String ETime) {
        this.ETime = ETime;
    }
}
