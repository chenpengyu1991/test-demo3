package com.founder.rhip.portal.dto;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

import java.io.Serializable;

@XmlRootElement(name = "Resp")
@XmlAccessorType(XmlAccessType.FIELD)
public class PatientInfoResponseFY implements Serializable {

	private static final long serialVersionUID = 577024078443982329L;

	private String TransactionCode;
	
	private String RespMessage;
	
	private String RespCode;
	
	private SickInfo SickInfo;
	
	public String getTransactionCode() {
		return TransactionCode;
	}

	public void setTransactionCode(String transactionCode) {
		TransactionCode = transactionCode;
	}

	public String getRespMessage() {
		return RespMessage;
	}

	public void setRespMessage(String respMessage) {
		RespMessage = respMessage;
	}

	public String getRespCode() {
		return RespCode;
	}

	public void setRespCode(String respCode) {
		RespCode = respCode;
	}

	public SickInfo getSickInfo() {
		return SickInfo;
	}

	public void setSickInfo(SickInfo sickInfo) {
		SickInfo = sickInfo;
	}

}
