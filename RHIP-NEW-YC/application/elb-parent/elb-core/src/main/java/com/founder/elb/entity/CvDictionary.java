	private Integer cn2;

	@Column(name = "CN3", columnDefinition = "NUMBER|扩展值类型字段3|8|", length = 8, nullable = true)
	private Integer cn3;

	@Column(name = "CN4", columnDefinition = "NUMBER|扩展值类型字段4|8|", length = 8, nullable = true)
	private Integer cn4;

	@Column(name = "CN5", columnDefinition = "NUMBER|扩展值类型字段5|8|", length = 8, nullable = true)
	private Integer cn5;

	public Long getId() {
		return this.id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Integer getDomainId() {
		return this.domainId;
	}

	public void setDomainId(Integer domainId) {
		this.domainId = domainId;
	}

	public String getCvDicmetaCode() {
		return this.cvDicmetaCode;
	}

	public void setCvDicmetaCode(String cvDicmetaCode) {
		this.cvDicmetaCode = cvDicmetaCode;
	}

	public String getCode() {
		return this.code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	/* 注：之前的方法名不符合规范 */
