package com.founder.rhip.ehr.common;


public enum Hospital {
	FirstPeopleHospital("46714063-X", "一院"),
	SecondPeopleHospital("46714062-1", "二院"),
	ThreePeopleHospital("46714117-3", "三院"),
	FivePeopleHospital("46714077-9", "五院"),
	ChineseMedicalHospital("46714078-7", "中医院");
	
	private String code;
	private String name;
	
	private Hospital(String code, String name) {
		this.code = code;
		this.name = name;
	}

	
	public String getCode() {
		return code;
	}

	
	public void setCode(String code) {
		this.code = code;
	}

	
	public String getName() {
		return name;
	}

	
	public void setName(String name) {
		this.name = name;
	}
	
	
}
